#include <stdio.h>

int main() {
    printf("Hello\tWorld!\n");       // \t for tab
    printf("This is a backslash: \\\n"); // \\ for backslash
    printf("Double quotes: \"Hello\"\n"); // \" for double quotes
    printf("New\nLine\n");           // \n for new line
    printf("Beep sound: \a\n");      // \a for beep sound

    return 0;
}
